package edu.udel.cisc475.aisim.simulation.communication;

public class SetRandomSeedMessage extends Message{
	private long seed;

	public SetRandomSeedMessage(String senderName, String destinationName,
			long seed) {
		super(senderName, destinationName);
		this.setSeed(seed);
	}

	public long getSeed() {
		return seed;
	}

	public void setSeed(long seed) {
		this.seed = seed;
	}
	
	
}
