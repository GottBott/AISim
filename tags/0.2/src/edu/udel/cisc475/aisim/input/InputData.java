package edu.udel.cisc475.aisim.input;

import java.util.ArrayList;

import edu.udel.cisc475.aisim.simulation.simulationstate.Agent;
import edu.udel.cisc475.aisim.tasktree.Node;

public class InputData {
	private Node treeHead;
	private ArrayList<Agent> agents;
	private String version;
	
	public InputData(Node treeHead, String version){
		this.treeHead = treeHead;
		agents = new ArrayList<Agent>();
		this.version = version;
	}

	public Node getTreeHead() {
		return treeHead;
	}

	public void setTreeHead(Node treeHead) {
		this.treeHead = treeHead;
	}

	public ArrayList<Agent> getAgents() {
		return agents;
	}

	public void setAgents(ArrayList<Agent> agents) {
		this.agents = agents;
	}

	public String getVersion() {
		return version;
	}

	public void setVersion(String version) {
		this.version = version;
	}
}
