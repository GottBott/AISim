package edu.udel.cisc475.aisim.input;

public class ConfigurationData {
	private int seed;
	private String inputLocation;
	private String outputDestination;
	
	final int DEFAULT_SEED = 0;
	final int DEFAULT_INPUT_LOCATION = 0;
	final int DEFAULT_OUTPUT_DESTINATION = 0;
	
	public ConfigurationData(int seed, String inputLocation, String outputDestination){
		this.seed = seed;
		this.inputLocation = inputLocation;
		this.outputDestination = outputDestination;
	}
	
	public ConfigurationData makeDefault(){
		return null;
	}
	
	public int getSeed() {
		return seed;
	}

	public void setSeed(int seed) {
		this.seed = seed;
	}

	public String getInputLocation() {
		return inputLocation;
	}

	public void setInputLocation(String inputLocation) {
		this.inputLocation = inputLocation;
	}

	public String getOutputDestination() {
		return outputDestination;
	}

	public void setOutputDestination(String outputDestination) {
		this.outputDestination = outputDestination;
	}
}
