package edu.udel.cisc475.aisim.input;

import java.util.ArrayList;

/**
 * This class represents an exception that gets thrown when there are errors in
 * the input .ctaems file.
 * 
 * @author Mike
 *
 */
public class InvalidInputException extends InvalidFileException {

	/**
	 * UID for serialization.
	 */
	private static final long serialVersionUID = 1L;

	/**
	 * The default constructor for an InvalidInputException.
	 * 
	 * @param errors
	 *            The list of errors that were found in the file.
	 */
	public InvalidInputException(ArrayList<FileError> errors) {
		super(errors);
	}

}
